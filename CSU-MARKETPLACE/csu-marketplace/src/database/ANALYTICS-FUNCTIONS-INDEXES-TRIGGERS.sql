-- Trigger: Detect and flag suspicious activity
CREATE OR REPLACE FUNCTION trigger_detect_suspicious_activity()
RETURNS TRIGGER AS $$
DECLARE
    v_rapid_orders INTEGER;
BEGIN
    -- Check for rapid order creation (more than 10 in 1 minute)
    SELECT COUNT(*) INTO v_rapid_orders
    FROM order_details
    WHERE buyer_id = NEW.buyer_id
    AND created_at >= NOW() - INTERVAL '1 minute';
    
    IF v_rapid_orders > 10 THEN
        -- Log suspicious activity
        INSERT INTO admin_logs (admin_id, action, target_user, description)
        VALUES (
            NULL,
            'SUSPICIOUS_ACTIVITY',
            NEW.buyer_id,
            'Rapid order creation: ' || v_rapid_orders || ' orders in 1 minute'
        );
    END IF;
    
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER detect_rapid_orders
    AFTER INSERT ON order_details
    FOR EACH ROW
    EXECUTE FUNCTION trigger_detect_suspicious_activity();

COMMENT ON TRIGGER detect_rapid_orders ON order_details IS 
    'Detect and log suspicious rapid order creation patterns';

