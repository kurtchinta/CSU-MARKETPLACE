-- Trigger: Auto-refresh materialized views on transaction completion
CREATE OR REPLACE FUNCTION trigger_refresh_analytics_on_transaction()
RETURNS TRIGGER AS $$
BEGIN
    -- Only refresh if transaction status changed to COMPLETED
    IF NEW.transaction_status = 'COMPLETED' AND OLD.transaction_status != 'COMPLETED' THEN
        -- Refresh materialized views (use CONCURRENTLY to avoid locks)
        REFRESH MATERIALIZED VIEW CONCURRENTLY mv_daily_platform_metrics;
        REFRESH MATERIALIZED VIEW CONCURRENTLY mv_transaction_funnel;
    END IF;
    
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER transaction_completed_refresh_analytics
    AFTER UPDATE ON transactions
    FOR EACH ROW
    WHEN (NEW.transaction_status = 'COMPLETED' AND OLD.transaction_status IS DISTINCT FROM 'COMPLETED')
    EXECUTE FUNCTION trigger_refresh_analytics_on_transaction();

COMMENT ON TRIGGER transaction_completed_refresh_analytics ON transactions IS 
    'Auto-refresh analytics materialized views when transaction completes';

