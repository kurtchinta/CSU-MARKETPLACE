-- Function: Calculate User Engagement Score
-- Returns: Engagement score (0-100) based on user activity
CREATE OR REPLACE FUNCTION fn_user_engagement_score(p_user_id UUID)
RETURNS DECIMAL(5, 2) AS $$
DECLARE
    v_score DECIMAL(5, 2) := 0;
    v_products_weight DECIMAL := 20;
    v_sales_weight DECIMAL := 30;
    v_orders_weight DECIMAL := 20;
    v_rating_weight DECIMAL := 20;
    v_activity_weight DECIMAL := 10;
BEGIN
    -- Products posted (max 20 points, capped at 10 products)
    v_score := v_score + LEAST((SELECT total_products_posted FROM users WHERE user_id = p_user_id) * 2, v_products_weight);
    
    -- Sales completed (max 30 points, capped at 15 sales)
    v_score := v_score + LEAST((SELECT total_products_sold FROM users WHERE user_id = p_user_id) * 2, v_sales_weight);
    
    -- Orders as buyer (max 20 points, capped at 10 orders)
    v_score := v_score + LEAST((SELECT total_orders_as_buyer FROM users WHERE user_id = p_user_id) * 2, v_orders_weight);
    
    -- Seller rating (max 20 points)
    v_score := v_score + ((SELECT average_seller_rating FROM users WHERE user_id = p_user_id) / 5 * v_rating_weight);
    
    -- Recent activity (max 10 points, based on days since last active)
    v_score := v_score + GREATEST(v_activity_weight - (SELECT EXTRACT(DAY FROM NOW() - COALESCE(last_active_at, created_at)) FROM users WHERE user_id = p_user_id), 0);
    
    RETURN LEAST(v_score, 100);
END;
$$ LANGUAGE plpgsql STABLE;

COMMENT ON FUNCTION fn_user_engagement_score IS 'Calculate user engagement score (0-100) based on activity metrics';

-- Function: Get Category Revenue Share
-- Returns: Percentage of total platform revenue
CREATE OR REPLACE FUNCTION fn_category_revenue_share(p_category_id INTEGER)
RETURNS DECIMAL(5, 2) AS $$
DECLARE
    v_category_revenue DECIMAL(12, 2);
    v_total_revenue DECIMAL(12, 2);
BEGIN
    SELECT COALESCE(SUM(item_price * quantity), 0) 
    INTO v_category_revenue
    FROM transactions 
    WHERE category_id = p_category_id AND transaction_status = 'COMPLETED';
    
    SELECT COALESCE(SUM(item_price * quantity), 0) 
    INTO v_total_revenue
    FROM transactions 
    WHERE transaction_status = 'COMPLETED';
    
    IF v_total_revenue = 0 THEN
        RETURN 0;
    END IF;
    
    RETURN ROUND((v_category_revenue / v_total_revenue) * 100, 2);
END;
$$ LANGUAGE plpgsql STABLE;

COMMENT ON FUNCTION fn_category_revenue_share IS 'Calculate category revenue as percentage of total platform revenue';

-- Function: Calculate Product Conversion Rate
-- Returns: Percentage of views that resulted in transactions
CREATE OR REPLACE FUNCTION fn_product_conversion_rate(p_product_id INTEGER)
RETURNS DECIMAL(5, 2) AS $$
DECLARE
    v_views INTEGER;
    v_sales INTEGER;
BEGIN
    SELECT view_count INTO v_views FROM products WHERE product_id = p_product_id;
    
    SELECT COUNT(*) INTO v_sales 
    FROM transactions 
    WHERE product_id = p_product_id AND transaction_status = 'COMPLETED';
    
    IF v_views = 0 THEN
        RETURN 0;
    END IF;
    
    RETURN ROUND((v_sales::DECIMAL / v_views) * 100, 2);
END;
$$ LANGUAGE plpgsql STABLE;

COMMENT ON FUNCTION fn_product_conversion_rate IS 'Calculate product conversion rate (sales/views * 100)';

-- Function: Get Seller Response Time (Average)
-- Returns: Average hours to accept/reject orders
CREATE OR REPLACE FUNCTION fn_seller_avg_response_time(p_seller_id UUID)
RETURNS DECIMAL(10, 2) AS $$
DECLARE
    v_avg_hours DECIMAL(10, 2);
BEGIN
    SELECT AVG(EXTRACT(EPOCH FROM (updated_at - created_at))/3600)
    INTO v_avg_hours
    FROM order_details
    WHERE seller_id = p_seller_id 
    AND order_status IN ('accepted', 'rejected')
    AND updated_at > created_at;
    
    RETURN COALESCE(v_avg_hours, 0);
END;
$$ LANGUAGE plpgsql STABLE;

COMMENT ON FUNCTION fn_seller_avg_response_time IS 'Calculate average seller response time in hours';

-- Function: Get User Lifetime Value (CLV)
-- Returns: Total value generated by user (buyer + seller)
CREATE OR REPLACE FUNCTION fn_user_lifetime_value(p_user_id UUID)
RETURNS DECIMAL(12, 2) AS $$
DECLARE
    v_seller_value DECIMAL(12, 2);
    v_buyer_value DECIMAL(12, 2);
BEGIN
    -- Revenue as seller
    SELECT COALESCE(SUM(item_price * quantity), 0)
    INTO v_seller_value
    FROM transactions
    WHERE seller_id = p_user_id AND transaction_status = 'COMPLETED';
    
    -- Spending as buyer
    SELECT COALESCE(SUM(item_price * quantity), 0)
    INTO v_buyer_value
    FROM transactions
    WHERE buyer_id = p_user_id AND transaction_status = 'COMPLETED';
    
    RETURN v_seller_value + v_buyer_value;
END;
$$ LANGUAGE plpgsql STABLE;

COMMENT ON FUNCTION fn_user_lifetime_value IS 'Calculate total user lifetime value (CLV) as buyer + seller';

-- Function: Get Platform Health Score
-- Returns: Overall platform health (0-100)
CREATE OR REPLACE FUNCTION fn_platform_health_score()
RETURNS DECIMAL(5, 2) AS $$
DECLARE
    v_score DECIMAL(5, 2) := 0;
    v_user_growth DECIMAL;
    v_transaction_growth DECIMAL;
    v_avg_rating DECIMAL;
    v_pending_ratio DECIMAL;
BEGIN
    -- User growth (20 points) - comparing last 7 days to previous 7 days
    SELECT CASE 
        WHEN prev_count > 0 THEN 
            LEAST((curr_count::DECIMAL / prev_count - 1) * 100, 20)
        ELSE 20
    END INTO v_user_growth
    FROM (
        SELECT 
            (SELECT COUNT(*) FROM users WHERE created_at >= NOW() - INTERVAL '7 days') as curr_count,
            (SELECT COUNT(*) FROM users WHERE created_at >= NOW() - INTERVAL '14 days' AND created_at < NOW() - INTERVAL '7 days') as prev_count
    ) growth;
    
    -- Transaction growth (30 points)
    SELECT CASE 
        WHEN prev_count > 0 THEN 
            LEAST((curr_count::DECIMAL / prev_count - 1) * 100, 30)
        ELSE 30
    END INTO v_transaction_growth
    FROM (
        SELECT 
            (SELECT COUNT(*) FROM transactions WHERE created_at >= NOW() - INTERVAL '7 days' AND transaction_status = 'COMPLETED') as curr_count,
            (SELECT COUNT(*) FROM transactions WHERE created_at >= NOW() - INTERVAL '14 days' AND created_at < NOW() - INTERVAL '7 days' AND transaction_status = 'COMPLETED') as prev_count
    ) tx_growth;
    
    -- Average platform rating (30 points)
    SELECT COALESCE(AVG(rating) / 5 * 30, 0) INTO v_avg_rating FROM reviews;
    
    -- Pending approval ratio (20 points) - lower is better
    SELECT 20 - LEAST((SELECT COUNT(*) FROM products WHERE status = 'PENDING')::DECIMAL / NULLIF((SELECT COUNT(*) FROM products), 0) * 100, 20)
    INTO v_pending_ratio;
    
    v_score := GREATEST(v_user_growth, 0) + GREATEST(v_transaction_growth, 0) + v_avg_rating + COALESCE(v_pending_ratio, 0);
    
    RETURN LEAST(v_score, 100);
END;
$$ LANGUAGE plpgsql STABLE;

COMMENT ON FUNCTION fn_platform_health_score IS 'Calculate overall platform health score (0-100) based on growth and engagement';

-- Function: Get Top Category for User
-- Returns: User's most active category
CREATE OR REPLACE FUNCTION fn_user_top_category(p_user_id UUID)
RETURNS TABLE (
    category_id INTEGER,
    category_name VARCHAR(100),
    product_count BIGINT
) AS $$
BEGIN
    RETURN QUERY
    SELECT 
        c.category_id,
        c.category_name,
        COUNT(p.product_id) as product_count
    FROM products p
    INNER JOIN categories c ON p.category_id = c.category_id
    WHERE p.user_id = p_user_id
    GROUP BY c.category_id, c.category_name
    ORDER BY product_count DESC
    LIMIT 1;
END;
$$ LANGUAGE plpgsql STABLE;

COMMENT ON FUNCTION fn_user_top_category IS 'Get user''s most active category by product count';

-- Function: Calculate Churn Risk Score
-- Returns: Churn risk (0-100, higher = more risk)
CREATE OR REPLACE FUNCTION fn_user_churn_risk(p_user_id UUID)
RETURNS DECIMAL(5, 2) AS $$
DECLARE
    v_days_inactive INTEGER;
    v_engagement_score DECIMAL(5, 2);
    v_churn_score DECIMAL(5, 2) := 0;
BEGIN
    SELECT EXTRACT(DAY FROM NOW() - COALESCE(last_active_at, created_at))
    INTO v_days_inactive
    FROM users WHERE user_id = p_user_id;
    
    v_engagement_score := fn_user_engagement_score(p_user_id);
    
    -- Inactivity component (0-50 points)
    v_churn_score := LEAST(v_days_inactive * 2, 50);
    
    -- Low engagement component (0-50 points)
    v_churn_score := v_churn_score + (50 - (v_engagement_score / 2));
    
    RETURN LEAST(v_churn_score, 100);
END;
$$ LANGUAGE plpgsql STABLE;

COMMENT ON FUNCTION fn_user_churn_risk IS 'Calculate user churn risk score (0-100, higher = more likely to churn)';

-- Function: Get Revenue Forecast (Simple Linear)
-- Returns: Projected revenue for next N days
CREATE OR REPLACE FUNCTION fn_revenue_forecast(p_days_ahead INTEGER DEFAULT 30)
RETURNS DECIMAL(12, 2) AS $$
DECLARE
    v_avg_daily_revenue DECIMAL(12, 2);
    v_growth_rate DECIMAL(5, 4);
BEGIN
    -- Calculate average daily revenue over last 30 days
    SELECT COALESCE(AVG(daily_revenue), 0)
    INTO v_avg_daily_revenue
    FROM (
        SELECT 
            DATE(completed_at) as date,
            SUM(item_price * quantity) as daily_revenue
        FROM transactions
        WHERE transaction_status = 'COMPLETED'
        AND completed_at >= NOW() - INTERVAL '30 days'
        GROUP BY DATE(completed_at)
    ) daily_rev;
    
    -- Simple growth rate calculation (last 7 days vs previous 7 days)
    SELECT CASE 
        WHEN prev_rev > 0 THEN (curr_rev / prev_rev - 1)
        ELSE 0
    END INTO v_growth_rate
    FROM (
        SELECT 
            (SELECT COALESCE(SUM(item_price * quantity), 0) FROM transactions WHERE transaction_status = 'COMPLETED' AND completed_at >= NOW() - INTERVAL '7 days') as curr_rev,
            (SELECT COALESCE(SUM(item_price * quantity), 0) FROM transactions WHERE transaction_status = 'COMPLETED' AND completed_at >= NOW() - INTERVAL '14 days' AND completed_at < NOW() - INTERVAL '7 days') as prev_rev
    ) growth_calc;
    
    -- Apply compound growth
    RETURN v_avg_daily_revenue * p_days_ahead * (1 + v_growth_rate);
END;
$$ LANGUAGE plpgsql STABLE;

COMMENT ON FUNCTION fn_revenue_forecast IS 'Forecast revenue for next N days using linear projection with growth rate';

-- Function: Get Admin Dashboard Summary
