-- Function: Accept Order and Create Transaction
CREATE OR REPLACE FUNCTION accept_order_and_create_transaction(
    p_order_id UUID,
    p_seller_final_pickup_location TEXT DEFAULT NULL,
    p_seller_final_meetup_location TEXT DEFAULT NULL
)
RETURNS UUID AS $$
DECLARE
    v_transaction_id UUID;
    v_order RECORD;
    v_buyer RECORD;
    v_seller RECORD;
    v_category RECORD;
BEGIN
    -- Get order details
    SELECT * INTO v_order
    FROM order_details
    WHERE order_id = p_order_id AND order_status = 'pending';
    
    IF NOT FOUND THEN
        RAISE EXCEPTION 'Order not found or already processed';
    END IF;
    
    -- Get buyer information
    SELECT user_id, first_name, last_name, email, phone_number 
    INTO v_buyer
    FROM users
    WHERE user_id = v_order.buyer_id;
    
    -- Get seller information
    SELECT user_id, first_name, last_name, email, phone_number 
    INTO v_seller
    FROM users
    WHERE user_id = v_order.seller_id;
    
    -- Get category information
    SELECT cat.category_id, cat.category_name 
    INTO v_category
    FROM categories cat
    INNER JOIN products p ON cat.category_id = p.category_id
    WHERE p.product_id = v_order.product_id;
    
    -- Update order status to accepted
    UPDATE order_details
    SET 
        order_status = 'accepted',
        final_pickup_location = COALESCE(p_seller_final_pickup_location, pickup_location),
        final_meetup_location = COALESCE(p_seller_final_meetup_location, meetup_location),
        updated_at = NOW()
    WHERE order_id = p_order_id;
    
    -- Create transaction record for blockchain
    INSERT INTO transactions (
        order_id,
        buyer_id,
        seller_id,
        product_id,
        category_id,
        buyer_name,
        seller_name,
        seller_phone,
        listing_type,
        category_name,
        item_name,
        item_description,
        item_price,
        quantity,
        final_pickup_location,
        final_meetup_location,
        return_condition,
        rental_duration,
        start_date,
        end_date,
        service_schedule,
        service_duration,
        message_to_seller,
        transaction_status,
        is_blockchain_pending,
        accepted_at,
        blockchain_tx_hash
    ) VALUES (
        p_order_id,
        v_order.buyer_id,
        v_order.seller_id,
        v_order.product_id,
        v_category.category_id,
        COALESCE(v_buyer.first_name || ' ' || v_buyer.last_name, 'Buyer'),
        COALESCE(v_seller.first_name || ' ' || v_seller.last_name, 'Seller'),
        v_seller.phone_number,
        v_order.listing_type,
        v_category.category_name,
        v_order.product_name,
        v_order.description,
        v_order.price,
        v_order.buyer_quantity,
        COALESCE(p_seller_final_pickup_location, v_order.pickup_location),
        COALESCE(p_seller_final_meetup_location, v_order.meetup_location),
        v_order.return_condition,
        v_order.rental_duration,
        v_order.start_date,
        v_order.end_date,
        v_order.service_schedule,
        v_order.service_duration,
        v_order.message_to_seller,
        'PENDING',
        TRUE,
        NOW(),
        gen_random_uuid()::TEXT
    ) RETURNING transaction_id INTO v_transaction_id;
    
    -- Create notification for buyer
    INSERT INTO notifications (user_id, title, message, type, related_order_id, related_transaction_id, created_at)
    VALUES (
        v_order.buyer_id,
        'Order Accepted',
        'Your order for ' || v_order.product_name || ' has been accepted! Transaction is being processed on blockchain.',
        'transaction',
        p_order_id,
        v_transaction_id,
        NOW()
    );
    
    RETURN v_transaction_id;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

COMMENT ON FUNCTION accept_order_and_create_transaction IS 
    'Accept a pending order and create transaction record for blockchain processing';

