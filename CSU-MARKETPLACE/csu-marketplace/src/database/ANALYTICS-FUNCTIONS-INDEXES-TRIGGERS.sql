-- Trigger: Update category view count
CREATE OR REPLACE FUNCTION trigger_update_category_views()
RETURNS TRIGGER AS $$
BEGIN
    IF NEW.view_count > OLD.view_count THEN
        UPDATE categories 
        SET total_views = total_views + (NEW.view_count - OLD.view_count)
        WHERE category_id = NEW.category_id;
    END IF;
    
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER product_view_update_category
    AFTER UPDATE OF view_count ON products
    FOR EACH ROW
    WHEN (NEW.view_count > OLD.view_count)
    EXECUTE FUNCTION trigger_update_category_views();

COMMENT ON TRIGGER product_view_update_category ON products IS 
