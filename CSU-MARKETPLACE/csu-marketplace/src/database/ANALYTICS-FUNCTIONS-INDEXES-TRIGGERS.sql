-- Trigger: Log admin actions
CREATE OR REPLACE FUNCTION trigger_log_admin_action()
RETURNS TRIGGER AS $$
BEGIN
    IF NEW.status != OLD.status THEN
        INSERT INTO admin_logs (admin_id, action, target_product, description)
        VALUES (
            NEW.approved_by,
            'PRODUCT_STATUS_CHANGE',
            NEW.product_id,
            'Changed status from ' || OLD.status || ' to ' || NEW.status
        );
    END IF;
    
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER product_status_change_log
    AFTER UPDATE OF status ON products
    FOR EACH ROW
    WHEN (NEW.status IS DISTINCT FROM OLD.status AND NEW.approved_by IS NOT NULL)
    EXECUTE FUNCTION trigger_log_admin_action();

COMMENT ON TRIGGER product_status_change_log ON products IS 
    'Log product approval/rejection actions to admin_logs';

