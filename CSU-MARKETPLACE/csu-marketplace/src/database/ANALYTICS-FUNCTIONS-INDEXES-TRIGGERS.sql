-- Function: Get chart data in JSON format
CREATE OR REPLACE FUNCTION fn_get_chart_data(p_chart_type VARCHAR(50), p_date_range INTEGER DEFAULT 30)
RETURNS JSONB AS $$
BEGIN
    CASE p_chart_type
        WHEN 'revenue_trend' THEN
            RETURN (
                SELECT jsonb_agg(row_to_json(t))
                FROM (
                    SELECT date, daily_revenue, cumulative_revenue
                    FROM analytics_view_revenue_trend
                    WHERE date >= CURRENT_DATE - p_date_range
                    ORDER BY date ASC
                ) t
            );
        
        WHEN 'category_performance' THEN
            RETURN (
                SELECT jsonb_agg(row_to_json(t))
                FROM (
                    SELECT category_name, total_revenue, completed_transactions
                    FROM analytics_view_category_performance
                    ORDER BY total_revenue DESC
                    LIMIT 10
                ) t
            );
        
        WHEN 'transaction_status' THEN
            RETURN (
                SELECT jsonb_agg(row_to_json(t))
                FROM analytics_view_transaction_status t
            );
        
        ELSE
            RETURN '{"error": "Unknown chart type"}'::JSONB;
    END CASE;
END;
$$ LANGUAGE plpgsql STABLE;

COMMENT ON FUNCTION fn_get_chart_data IS 
    'Get pre-formatted JSON data for frontend charts. Usage: SELECT fn_get_chart_data(''revenue_trend'', 30)';

